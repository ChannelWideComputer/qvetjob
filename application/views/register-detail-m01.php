                        <h3>กฎเเละข้อตกลง </h3>
                        <hr style="border-top: 1px solid #ed4a29;">

                        <p>ตามที่ท่านตกลงเข้าเป็นสมาชิกกับ บริษัท ควอลิไฟด์ เอสเซนส์ ซัพพลายออร์ แอนด์ เทรเซอร์ จำกัด (“บริษัท”) เพื่อปฏิบัติงานให้แก่บริษัท ท่านรับทราบและตกลงผูกพันในเงื่อนไขดังต่อไปนี้</p>
                        <br>
                        <p>1. เมื่อท่านเป็นสมาชิกกับบริษัทแล้ว ท่านสามารถเลือกวันที่ประสงค์ปฏิบัติงานและเปิดดูตารางงานของท่าน ตามที่ท่านต้องการใน WWW.QVETJOB.COM ได้ทันที </p>
                        <br>
                        <p>2. หากท่านลงตารางงานแล้วแต่ไม่สามารถไปปฏิบัติงานได้ ท่านต้องแจ้งแก้ไขหรือยกเลิกตารางงานก่อนถึงวันเริ่มงานไม่น้อยกว่า 3 วัน</p>
                        <br>
                        <p>3. หากท่านไม่สามารถปฏิบัติงานตามที่ท่านได้ลงตารางงานไว้โดยไม่แจ้งให้บริษัททราบล่วงหน้าตามข้อ 2 และบริษัทพิจารณาแล้วเห็นว่าปราศจากเหตุผลอันสมควร บริษัทมีสิทธิพิจารณาตัดสิทธิ์การเป็นสมาชิกของท่านทันที โดยท่านจะไม่สามารถสมัครเป็นสมาชิกได้อีกเป็นเวลา 1 ปี</p>
                        <br>
                        <p>4. เมื่อท่านปฏิบัติงานเสร็จสิ้นในแต่ละวัน บริษัทจะโอนเงินเข้าระบบบัญชีของท่าน ตามหมายเลขบัญชีที่ท่านได้แจ้งให้บริษัททราบภายใน 5 วันทำการ โดยบริษัทจะหักภาษี ณ ที่จ่าย ตามอัตราที่กฎหมายกำหนด</p>
                        <br>
                        <p>5. ท่านขอรับรองว่าข้อมูลและประวัติที่ท่านแจ้งในใบสมัคร เช่น ประวัติส่วนตัว ความสามารถในการรักษาที่ระบุ  ใบประกอบวิชาชีพ เป็นข้อมูลที่ถูกต้องตรงตามความเป็นจริงทุกประการ หากพบว่ามีข้อใดเป็นเท็จหรือไม่ถูกต้องตามกฏหมาย บริษัทมีสิทธิยกเลิกการเป็นสมาชิก หรือบอกเลิกการจ้างได้ทันที อีกทั้งบริษัทมีสิทธิเรียกร้องค่าเสียหายจากท่าน หากการแจ้งข้อมูลอันเป็นเท็จก่อให้เกิดความเสียหายอย่างใดอย่างหนึ่งแก่บริษัท และ/หรือโรงพยาบาลสัตว์ หรือ คลินิก</p>
                        <br>
                        <p>6. ในระหว่างปฏิบัติงานตามข้อตกลงนี้ หากท่านได้กระทำการอย่างใดอย่างหนึ่งอันก่อให้เกิดความเสียหายแก่คลินิก หรือ โรงพยาบาลสัตว์ และบริษัทได้ชดใช้ค่าเสียหาย หรือค่าสินไหมทดแทนแก่คลินิก หรือโรงพยาบาลสัตว์ บริษัทขอสงวนสิทธิ์ในการเรียกเก็บเงินส่วนดังกล่าวกับท่านพร้อมดอกเบี้ยตามกฎหมาย </p>
                        <br>
